const NAME = 'Victor';
console.log('Good Morning, ' + NAME);
console.log('Good Afternoon, ' + NAME);
console.log('Good Evening, ' + NAME);

NAME = 'Joe';
console.log('Good Morning, ' + NAME);
console.log('Good Afternoon, ' + NAME);
console.log('Good Evening, ' + NAME);

// This returns a TypError. This code attempts to re-assign NAME to 'Joe' but it can not be re-assigned because NAME is a constant. Constants can not be re-assigned.